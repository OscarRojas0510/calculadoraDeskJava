    public void llenarTabla(Object o[][])
    {
        DefaultTableModel dfm = new DefaultTableModel();
        jTable1.setModel(dfm);
        dfm.addColumn("Fecha inicio");
        dfm.addColumn("Fecha fin");
        dfm.addColumn("Sueldo");
        dfm.addColumn("Semanas");
        for (int i = 0; i < o.length; i++)
        {
            dfm.addRow(o[i]);
        }
    }

    private void init2(Object o[][])
    {
        this.rowH = jTable1.getRowHeight();
        this.rowW = jTable1.getColumnModel().getColumn(0).getPreferredWidth();
        this.fontS = jTable1.getFont().getSize();
        llenarTabla(o);
    }
