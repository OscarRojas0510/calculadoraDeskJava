    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton3ActionPerformed
    {//GEN-HEADEREND:event_jButton3ActionPerformed
        // TODO add your handling code here:
        rowH -= 20;
        rowW -= 20;
        fontS -= 2;
        if (jTable1.getColumnModel().getColumnCount() > 0)
        {
            if (rowW > 1)
            {
                jTable1.getColumnModel().getColumn(0).setPreferredWidth(rowW);
                jTable1.getColumnModel().getColumn(1).setPreferredWidth(rowW);
                jTable1.getColumnModel().getColumn(2).setPreferredWidth(rowW);
                jTable1.getColumnModel().getColumn(3).setPreferredWidth(rowW);
            } else
            {
                rowW = 1;
            }
            if (rowH > 1)
            {
                jTable1.setRowHeight(rowH);
            } else
            {
                rowH = 1;
            }
            if (fontS >= 12)
            {
                jTable1.setFont(new java.awt.Font("Segoe UI", 0, fontS));
            } else
            {
                fontS = 10;
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

